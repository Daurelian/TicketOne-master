package entities;

public class Sala {
    private int id_sala;
    private String nome;
    private int num_posti;
    private int id_sede;

    public int getId_sala() {
        return id_sala;
    }

    public String getNome() {
        return nome;
    }

    public int getNum_posti() {
        return num_posti;
    }

    public int getId_sede() {
        return id_sede;
    }
}
